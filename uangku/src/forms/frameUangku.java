    public void tampiltableuangku(){
        try {
            modeltabel.getDataVector().removeAllElements();
            modeltabel.fireTableDataChanged();
            
            String sqldata = "select*from uangku order by id ASC";
            PreparedStatement querydata = objekku.konek.prepareStatement(sqldata);
            ResultSet barisdata = querydata.executeQuery();
            while(barisdata.next()){
                Object [] x = new Object[6];
                x[0] = barisdata.getString("id");
                x[1] = barisdata.getString("tanggal");
                x[2] = barisdata.getString("keterangan");
                x[3] = barisdata.getString("pemasukan");
                x[4] = barisdata.getString("pengeluaran");
                x[5] = barisdata.getString("saldo");
                
                modeltabel.addRow(x);
            } 
            barisdata.close();
            querydata.close();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

