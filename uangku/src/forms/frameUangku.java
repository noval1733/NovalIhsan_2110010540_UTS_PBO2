    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        try {
            if (txtid.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "NPM belum di isi");
                txtid.requestFocus();//delphi setfokuc
            } else if (txttanggal.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Nama belum di isi");
                txttanggal.requestFocus();
            } else if (txtket.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Telepon belum di isi");
                txtket.requestFocus();
            } else if (txtpemasukan.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Telepon belum di isi");
                txtpemasukan.requestFocus();
            } else if (txtpengeluaran.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Telepon belum di isi");
                txtpengeluaran.requestFocus();
            } else if (txtsaldo.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Telepon belum di isi");
                txtsaldo.requestFocus();
            } else {
                String sqlcari = "select * from uangku where id=?";
                PreparedStatement cari = objekku.konek.prepareStatement(sqlcari);
                cari.setString(1, txtid.getText());
                ResultSet baris = cari.executeQuery();
                if (baris.next()){
                    JOptionPane.showMessageDialog(this, "ID sudah ada");
                    txttanggal.setText(baris.getString("tanggal"));
                    txtket.setText(baris.getString("keterangan"));
                    txtpemasukan.setText(baris.getString("pemasukan"));
                    txtpengeluaran.setText(baris.getString("pengeluaran"));
                    txtsaldo.setText(baris.getString("saldo"));
                } else {
                    String sqlsimpan = "insert into uangku (id, tanggal, keterangan, pemasukan, pengeluaran, saldo) value (?,?,?,?,?,?)";
                    PreparedStatement simpan = objekku.konek.prepareStatement(sqlsimpan);
                    simpan.setString(1, txtid.getText());
                    simpan.setString(2, txttanggal.getText());
                    simpan.setString(3, txtket.getText());
                    simpan.setString(4, txtpemasukan.getText());
                    simpan.setString(5, txtpengeluaran.getText());
                    simpan.setString(6, txtsaldo.getText());
                    simpan.executeUpdate();
                    tampiltableuangku();
                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan");                    
                }
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btntambahActionPerformed

